#!/usr/bin/env tsx
/**
 * Generates package.json, Gemfile, and shared config into templates.
 *
 * - package.json: shared/npm/<stack>.json → templates/<stack>/package.json
 * - Gemfile:      shared/gemfile/Gemfile.<stack> → templates/<stack>/Gemfile
 *
 * フレームワーク別（next.config.ts, vite*.ts など）は templates/<stack>/ 直下を正本として編集する。
 *
 * Run: yarn generate:deps
 */

import { readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import {
  NPM_STACKS,
  GEMFILE_STACKS,
  STACK_DEFINITIONS,
  TEMPLATES_DIR,
} from './lib/stacks.js';
import { ROOT, SHARED_NPM, SHARED_GEMFILE, deepMerge } from './lib/utils.js';
import { logger } from './lib/logger.js';
import { handleGenerationError } from './lib/errors.js';

const GEMFILE_HEADER =
  '# Generated by scripts/generate-deps.ts — edit shared/gemfile/ instead.\n\n';

function run(): void {
  for (const stack of NPM_STACKS) {
    const srcPath = join(SHARED_NPM, `${stack}.json`);
    const content = readFileSync(srcPath, 'utf8');
    const outPath = join(ROOT, TEMPLATES_DIR, stack, 'package.json');
    writeFileSync(outPath, content + '\n', 'utf8');
    logger.generated(outPath);
  }

  for (const stack of GEMFILE_STACKS) {
    const srcPath = join(SHARED_GEMFILE, `Gemfile.${stack}`);
    const content = readFileSync(srcPath, 'utf8');
    const outPath = join(ROOT, TEMPLATES_DIR, stack, 'Gemfile');
    writeFileSync(outPath, GEMFILE_HEADER + content, 'utf8');
    logger.generated(outPath);
  }

  // full-templates: package.json を shared/npm/<stack>.json + <slug>.diff.json のマージで生成
  for (const s of STACK_DEFINITIONS.filter(
    (s) => s.fullDir != null && s.hasNpm,
  )) {
    const stackSlug = s.dir.replace(new RegExp(`^${TEMPLATES_DIR}/`), '');
    const basePath = join(SHARED_NPM, `${stackSlug}.json`);
    const base = JSON.parse(readFileSync(basePath, 'utf8'));
    let merged = base;
    if (s.fullNpmDiffSlug != null) {
      const diffPath = join(SHARED_NPM, `${s.fullNpmDiffSlug}.diff.json`);
      const diff = JSON.parse(readFileSync(diffPath, 'utf8'));
      merged = deepMerge(base, diff);
    }
    mkdirSync(join(ROOT, s.fullDir!), { recursive: true });
    const outPath = join(ROOT, s.fullDir!, 'package.json');
    writeFileSync(outPath, JSON.stringify(merged, null, 2) + '\n', 'utf8');
    logger.generated(outPath);
  }
}

try {
  run();
} catch (error) {
  handleGenerationError(error);
}
