#!/usr/bin/env tsx
/**
 * Generates package.json, Gemfile, and shared config into templates.
 *
 * - package.json: shared/npm/<stack>.json → templates/<stack>/package.json
 * - Gemfile:      shared/gemfile/Gemfile.<stack> → templates/<stack>/Gemfile
 * - C# global.json: templates/csharp/global.json の sdk.version を shared/versions.json の dotnet で上書き
 * - Go:           .golangci.yml は templates/go/ を正本として編集
 * - Rust:         rust-toolchain.toml は templates/rust/ を正本として編集
 *
 * フレームワーク別（next.config.ts, vite.config.ts, Laravel pint/phpunit, C# .csproj/.sln）は
 * templates/<stack>/ 直下を正本として編集する。
 *
 * Run: yarn generate:deps
 */

import { existsSync, readFileSync, writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import { NPM_STACKS, GEMFILE_STACKS, STACK_DEFINITIONS, TEMPLATES_DIR } from './lib/stacks.js';
import { ROOT, VERSIONS, SHARED_NPM, SHARED_GEMFILE, deepMerge } from './lib/utils.js';

const GEMFILE_HEADER =
  '# Generated by scripts/generate-deps.ts — edit shared/gemfile/ instead.\n\n';

// Mapping of npm stacks to their base file names and optional diff file names
const NPM_DIFF_MAPPING: Record<string, { base: string; diff?: string }> = {
  'rails-api': { base: 'rails', diff: 'rails-api' },
  'sinatra': { base: 'ruby', diff: 'sinatra' },
};

function run(): void {
  for (const stack of NPM_STACKS) {
    // Determine base file name and optional diff file name
    const mapping = NPM_DIFF_MAPPING[stack];
    const baseFileName = mapping?.base ?? stack;
    const diffFileName = mapping?.diff;

    const basePath = join(SHARED_NPM, `${baseFileName}.json`);
    const base = JSON.parse(readFileSync(basePath, 'utf8'));

    let merged = base;
    if (diffFileName != null) {
      const diffPath = join(SHARED_NPM, `${diffFileName}.diff.json`);
      const diff = JSON.parse(readFileSync(diffPath, 'utf8'));
      merged = deepMerge(base, diff);
    }

    const outPath = join(ROOT, TEMPLATES_DIR, stack, 'package.json');
    writeFileSync(outPath, JSON.stringify(merged, null, 2) + '\n', 'utf8');
    console.log('Generated:', outPath);
  }

  for (const stack of GEMFILE_STACKS) {
    const srcPath = join(SHARED_GEMFILE, `Gemfile.${stack}`);
    const content = readFileSync(srcPath, 'utf8');
    const outPath = join(ROOT, TEMPLATES_DIR, stack, 'Gemfile');
    writeFileSync(outPath, GEMFILE_HEADER + content, 'utf8');
    console.log('Generated:', outPath);
  }

  // C#: templates/csharp/global.json の sdk.version を versions.json の dotnet で上書き
  const csharpGlobalPath = join(ROOT, TEMPLATES_DIR, 'csharp', 'global.json');
  if (VERSIONS.dotnet && existsSync(csharpGlobalPath)) {
    const globalJson = JSON.parse(readFileSync(csharpGlobalPath, 'utf8')) as {
      sdk: { version: string; rollForward?: string };
    };
    globalJson.sdk.version = VERSIONS.dotnet;
    writeFileSync(
      csharpGlobalPath,
      JSON.stringify(globalJson, null, 2) + '\n',
      'utf8',
    );
    console.log('Updated:', csharpGlobalPath);
  }

  // Django/Laravel: 設定のみ管理するテンプレートのディレクトリを事前作成
  mkdirSync(join(ROOT, TEMPLATES_DIR, 'django'), { recursive: true });
  mkdirSync(join(ROOT, TEMPLATES_DIR, 'laravel'), { recursive: true });

  // full-templates: package.json を shared/npm/<stack>.json + <slug>.diff.json のマージで生成
  for (const s of STACK_DEFINITIONS.filter((s) => s.fullDir != null && s.hasNpm)) {
    const stackSlug = s.dir.replace(new RegExp(`^${TEMPLATES_DIR}/`), '');

    // Use NPM_DIFF_MAPPING to find base file (handles rails-api, sinatra cases)
    const mapping = NPM_DIFF_MAPPING[stackSlug];
    const baseFileName = mapping?.base ?? stackSlug;

    const basePath = join(SHARED_NPM, `${baseFileName}.json`);
    const base = JSON.parse(readFileSync(basePath, 'utf8'));
    let merged = base;
    if (s.fullNpmDiffSlug != null) {
      const diffPath = join(SHARED_NPM, `${s.fullNpmDiffSlug}.diff.json`);
      const diff = JSON.parse(readFileSync(diffPath, 'utf8'));
      merged = deepMerge(base, diff);
    }
    mkdirSync(join(ROOT, s.fullDir!), { recursive: true });
    const outPath = join(ROOT, s.fullDir!, 'package.json');
    writeFileSync(outPath, JSON.stringify(merged, null, 2) + '\n', 'utf8');
    console.log('Generated:', outPath);
  }
}

run();
