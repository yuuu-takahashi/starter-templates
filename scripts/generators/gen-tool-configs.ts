export const generatePlaywright = async (): Promise<void> => {
  logger.debug('Generating Playwright configs');
  const playwrightPath = join(SHARED_PLAYWRIGHT, 'playwright.config.nextjs.ts');
  ensureFileExists(playwrightPath, 'playwright');
  const GEN_HEADER =
    '// Generated by scripts/commands/generate-configs.ts — edit shared/extensions/playwright/ instead.\n\n';
  const PLAYWRIGHT_SPECS_SOURCE = join(SHARED_PLAYWRIGHT, 'specs');

  const PLAYWRIGHT_DIRS = ['full-templates/nextjs'];
  for (const dir of PLAYWRIGHT_DIRS) {
    const outPath = join(ROOT, dir, 'playwright.config.ts');
    writeFileSync(outPath, GEN_HEADER + readFileSync(playwrightPath, 'utf8'), 'utf8');
    logger.generated(outPath);

    // tests/e2e/specs ディレクトリ作成とサンプル spec コピー
    const e2eDir = join(ROOT, dir, 'tests', 'e2e', 'specs');
    mkdirSync(e2eDir, { recursive: true });
    if (existsSync(PLAYWRIGHT_SPECS_SOURCE)) {
      const specFiles = readdirSync(PLAYWRIGHT_SPECS_SOURCE) as string[];
      for (const file of specFiles.filter((f: string) => f.endsWith('.spec.ts'))) {
        const content = readFileSync(join(PLAYWRIGHT_SPECS_SOURCE, file), 'utf8');
        writeFileSync(join(e2eDir, file), content, 'utf8');
        logger.generated(join(dir, 'tests', 'e2e', 'specs', file));
      }
    }
  }
}

export const generateMocks = async (): Promise<void> => {
  logger.debug('Generating MSW mocks');
  const MOCKS_DIRS = ['full-templates/nextjs'];
  const GEN_HEADER =
    '// Generated by scripts/commands/generate-configs.ts — edit shared/config/test/mocks/ instead.\n\n';

  for (const dir of MOCKS_DIRS) {
    const mocksOut = join(ROOT, dir, 'src', 'mocks');
    mkdirSync(mocksOut, { recursive: true });
    for (const file of ['handlers.ts', 'server.ts']) {
      const mocksFilePath = join(SHARED_MOCKS, file);
      ensureFileExists(mocksFilePath, 'mocks');
      const content = readFileSync(mocksFilePath, 'utf8');
      writeFileSync(join(mocksOut, file), GEN_HEADER + content, 'utf8');
      logger.generated(join(dir, 'src', 'mocks', file));
    }
  }
}

export async function run(): Promise<void> {
  try {
    logger.start('Tool Configs Generation');

    // Generate configs concurrently where possible
    // Group 1: Independent tool configs (can run in parallel)
    await Promise.all([generateESLint(), generatePrettier()]);

    // Group 2: Template-specific configs (sequential to avoid conflicts)
    await generateVitest();
    await generateTsconfig();

    // Group 3: Markup/format tools (can run in parallel)
    await Promise.all([generateMarkuplint(), generateMarkdownlint(), generateLighthouse()]);

    // Group 4: Advanced tools (can run in parallel)
    await Promise.all([generateKnip(), generatePlaywright(), generateMocks()]);

    logger.end('Tool Configs Generation');
  } catch (error) {
    if (error instanceof GenerationError) {
      logger.error(`${error.message}`, error);
    } else if (error instanceof Error) {
      logger.error(`Tool config generation failed: ${error.message}`, error);
    } else {
      logger.error('Unknown error during tool config generation');
    }
    process.exit(1);
  }
}
