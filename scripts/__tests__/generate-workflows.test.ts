import { describe, it, expect } from 'vitest';
import { existsSync, readFileSync } from 'fs';
import { join } from 'path';
import { ROOT } from '../lib/utils.js';
import {
  TEMPLATE_DIRS,
  FULL_TEMPLATE_DIRS,
  CODE_CHECK_SOURCE,
  FULL_CODE_CHECK_SOURCE,
  TEST_SOURCE,
  FULL_TEST_SOURCE,
  FULL_CI_SOURCE,
} from '../lib/stacks.js';

const WORKFLOWS_DIR = join(ROOT, 'shared', 'workflows');

const ALL_TEMPLATE_DIRS = [...TEMPLATE_DIRS, ...FULL_TEMPLATE_DIRS];

// ── ソースファイル存在確認 ────────────────────────────────────────────────────

describe('gen-workflows: ソースワークフローファイル', () => {
  const allCodeCheckSources = {
    ...CODE_CHECK_SOURCE,
    ...FULL_CODE_CHECK_SOURCE,
  };
  const uniqueCodeCheck = [...new Set(Object.values(allCodeCheckSources))];

  it.each(uniqueCodeCheck)(
    'shared/workflows/%s が存在する (code-check)',
    (name) => {
      expect(existsSync(join(WORKFLOWS_DIR, name))).toBe(true);
    },
  );

  const allTestSources = { ...TEST_SOURCE, ...FULL_TEST_SOURCE };
  const uniqueTest = [...new Set(Object.values(allTestSources))];

  it.each(uniqueTest)('shared/workflows/%s が存在する (test)', (name) => {
    expect(existsSync(join(WORKFLOWS_DIR, name))).toBe(true);
  });

  const uniqueCi = [...new Set(Object.values(FULL_CI_SOURCE))];

  it.each(uniqueCi)('shared/workflows/%s が存在する (ci)', (name) => {
    expect(existsSync(join(WORKFLOWS_DIR, name))).toBe(true);
  });

  it('shared/workflows/static-analysis.yml が存在する', () => {
    expect(existsSync(join(WORKFLOWS_DIR, 'static-analysis.yml'))).toBe(true);
  });

  it('shared/workflows/dependabot.yml が存在する', () => {
    expect(existsSync(join(WORKFLOWS_DIR, 'dependabot.yml'))).toBe(true);
  });
});

// ── 生成済み code-check.yml ──────────────────────────────────────────────────

describe('gen-workflows: 生成済み code-check.yml', () => {
  const allDirs = Object.keys({
    ...CODE_CHECK_SOURCE,
    ...FULL_CODE_CHECK_SOURCE,
  });

  it.each(allDirs)('%s/.github/workflows/code-check.yml が存在する', (dir) => {
    expect(
      existsSync(join(ROOT, dir, '.github', 'workflows', 'code-check.yml')),
    ).toBe(true);
  });

  it.each(allDirs)(
    '%s/.github/workflows/code-check.yml が生成ヘッダーを持つ',
    (dir) => {
      const content = readFileSync(
        join(ROOT, dir, '.github', 'workflows', 'code-check.yml'),
        'utf8',
      );
      expect(content).toContain('# Generated by scripts/generate-configs.ts');
    },
  );

  it.each(allDirs)(
    '%s/.github/workflows/code-check.yml に on: が含まれる',
    (dir) => {
      const content = readFileSync(
        join(ROOT, dir, '.github', 'workflows', 'code-check.yml'),
        'utf8',
      );
      expect(content).toContain('on:');
    },
  );

  it.each(allDirs)(
    '%s/.github/workflows/code-check.yml に jobs: が含まれる',
    (dir) => {
      const content = readFileSync(
        join(ROOT, dir, '.github', 'workflows', 'code-check.yml'),
        'utf8',
      );
      expect(content).toContain('jobs:');
    },
  );
});

