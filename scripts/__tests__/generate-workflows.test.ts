// ── test.yml を持たないテンプレートの確認 ───────────────────────────────────

describe('gen-workflows: test.yml のないテンプレート', () => {
  const withTest = new Set(Object.keys(TEST_SOURCE));
  const withoutTest = [...TEMPLATE_DIRS].filter((dir) => !withTest.has(dir));

  it('testWorkflow 未定義のテンプレートが存在する（ruby のみ）', () => {
    expect(withoutTest.length).toBeGreaterThan(0);
  });

  it.each(withoutTest)('%s に test.yml が生成されていない', (dir) => {
    expect(
      existsSync(join(ROOT, dir, '.github', 'workflows', 'test.yml')),
    ).toBe(false);
  });
});
