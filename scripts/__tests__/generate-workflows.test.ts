// ── 生成済み dependabot.yml ──────────────────────────────────────────────────

describe('gen-workflows: 生成済み dependabot.yml', () => {
  it.each(ALL_TEMPLATE_DIRS)('%s/.github/dependabot.yml が存在する', (dir) => {
    expect(existsSync(join(ROOT, dir, '.github', 'dependabot.yml'))).toBe(true);
  });

  it.each(ALL_TEMPLATE_DIRS)(
    '%s/.github/dependabot.yml が生成ヘッダーを持つ',
    (dir) => {
      const content = readFileSync(
        join(ROOT, dir, '.github', 'dependabot.yml'),
        'utf8',
      );
      expect(content).toContain('# Generated by scripts/generate-configs.ts');
    },
  );

  it('全テンプレートの dependabot.yml が同一内容である', () => {
    const contents = ALL_TEMPLATE_DIRS.map((dir) =>
      readFileSync(join(ROOT, dir, '.github', 'dependabot.yml'), 'utf8'),
    );
    const first = contents[0];
    for (const c of contents.slice(1)) {
      expect(c).toBe(first);
    }
  });
});

