describe('gen-tool-configs: 生成済み tsconfig', () => {
  const TSCONFIG_FILES = [
    { path: `${td}/react/tsconfig.json` },
    { path: `${td}/react/tsconfig.app.json` },
    { path: `${td}/react/tsconfig.node.json` },
    { path: `${td}/nextjs/tsconfig.json` },
    { path: 'full-templates/nextjs/tsconfig.json' },
  ];

  it.each(TSCONFIG_FILES)('$path が存在する', ({ path }) => {
    expect(existsSync(join(ROOT, path))).toBe(true);
  });

  it.each(TSCONFIG_FILES)('$path が有効な JSON である', ({ path }) => {
    const raw = readFileSync(join(ROOT, path), 'utf8');
    expect(() => JSON.parse(raw)).not.toThrow();
  });

  it.each(TSCONFIG_FILES)('$path が compilerOptions を持つ', ({ path }) => {
    const config = JSON.parse(readFileSync(join(ROOT, path), 'utf8')) as Record<
      string,
      unknown
    >;
    // tsconfig.json (root) は references を持つ場合がある
    const hasCompilerOptions = 'compilerOptions' in config;
    const hasReferences = 'references' in config;
    expect(hasCompilerOptions || hasReferences).toBe(true);
  });

  it('minimal nextjs の tsconfig は strict が有効である', () => {
    const config = JSON.parse(
      readFileSync(join(ROOT, `${td}/nextjs/tsconfig.json`), 'utf8'),
    ) as { compilerOptions?: { strict?: boolean } };
    expect(config.compilerOptions?.strict).toBe(true);
  });
});
