import { describe, it, expect } from 'vitest';
import { existsSync, readFileSync } from 'fs';
import { join } from 'path';
import {
  ROOT,
  SHARED_CONFIG_STACKS,
  SHARED_ESLINT,
  SHARED_PRETTIER,
  SHARED_VITEST,
  SHARED_TSCONFIG,
} from '../lib/utils.js';
import { TEMPLATES_DIR } from '../lib/stacks.js';
import { ESLINT_SOURCE } from '../generate-tool-configs.js';

const td = TEMPLATES_DIR;

describe('gen-tool-configs: ESLint ソースファイル', () => {
  it.each(ESLINT_SOURCE)(
    '$source が shared/lint-format/eslint/ に存在する',
    ({ source }) => {
      expect(existsSync(join(SHARED_ESLINT, source))).toBe(true);
    },
  );
});

describe('gen-tool-configs: 生成済み ESLint 設定', () => {
  it.each(ESLINT_SOURCE)('$dir/eslint.config.js が存在する', ({ dir }) => {
    expect(existsSync(join(ROOT, dir, 'eslint.config.js'))).toBe(true);
  });

  it.each(ESLINT_SOURCE)(
    '$dir/eslint.config.js が生成ヘッダーで始まる',
    ({ dir }) => {
      const content = readFileSync(join(ROOT, dir, 'eslint.config.js'), 'utf8');
      expect(content).toContain('// Generated by scripts/generate-configs.ts');
    },
  );

  it.each(ESLINT_SOURCE)(
    '$dir/eslint.config.js がヘッダー以外の内容を持つ',
    ({ dir }) => {
      const content = readFileSync(join(ROOT, dir, 'eslint.config.js'), 'utf8');
      const lines = content
        .split('\n')
        .filter((l) => l.trim() && !l.startsWith('//'));
      expect(lines.length).toBeGreaterThan(0);
    },
  );
});

// ── Prettier ─────────────────────────────────────────────────────────────────

describe('gen-tool-configs: Prettier ソースファイル', () => {
  it('.prettierrc.ts が shared/lint-format/prettier/ に存在する', () => {
    expect(existsSync(join(SHARED_PRETTIER, '.prettierrc.ts'))).toBe(true);
  });
});

describe('gen-tool-configs: 生成済み Prettier 設定', () => {
  it.each([...SHARED_CONFIG_STACKS])(
    '%s/.prettierrc.json が存在する',
    (dir) => {
      expect(existsSync(join(ROOT, dir, '.prettierrc.json'))).toBe(true);
    },
  );

  it.each([...SHARED_CONFIG_STACKS])(
    '%s/.prettierrc.json が有効な JSON である',
    (dir) => {
      const raw = readFileSync(join(ROOT, dir, '.prettierrc.json'), 'utf8');
      expect(() => JSON.parse(raw)).not.toThrow();
    },
  );

  it.each([...SHARED_CONFIG_STACKS])(
    '%s/.prettierrc.json が必須キーを持つ',
    (dir) => {
      const config = JSON.parse(
        readFileSync(join(ROOT, dir, '.prettierrc.json'), 'utf8'),
      ) as Record<string, unknown>;
      expect(config).toHaveProperty('semi');
      expect(config).toHaveProperty('singleQuote');
      expect(config).toHaveProperty('printWidth');
      expect(config).toHaveProperty('tabWidth');
    },
  );

  it('全テンプレートの .prettierrc.json が同一内容である', () => {
    const contents = [...SHARED_CONFIG_STACKS].map((dir) =>
      readFileSync(join(ROOT, dir, '.prettierrc.json'), 'utf8'),
    );
    const first = contents[0];
    for (const c of contents.slice(1)) {
      expect(c).toBe(first);
    }
  });
});

